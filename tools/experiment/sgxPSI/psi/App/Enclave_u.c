static const struct {
	size_t nr_ocall;
	void * table[7];
} ocall_table_Enclave = {
	7,
	{
		(void*)Enclave_print,
		(void*)Enclave_printUint,
		(void*)Enclave_sgx_oc_cpuidex,
		(void*)Enclave_sgx_thread_wait_untrusted_event_ocall,
		(void*)Enclave_sgx_thread_set_untrusted_event_ocall,
		(void*)Enclave_sgx_thread_setwait_untrusted_events_ocall,
		(void*)Enclave_sgx_thread_set_multiple_untrusted_events_ocall,
	}
};
sgx_status_t upload_server_data(sgx_enclave_id_t eid, sgx_status_t* retval, const uint64_t* server_data, size_t server_data_size)
{
	sgx_status_t status;
	ms_upload_server_data_t ms;
	ms.ms_server_data = server_data;
	ms.ms_server_data_size = server_data_size;
	status = sgx_ecall(eid, 0, &ocall_table_Enclave, &ms);
	if (status == SGX_SUCCESS && retval) *retval = ms.ms_retval;
	return status;
}

sgx_status_t upload_and_psi(sgx_enclave_id_t eid, sgx_status_t* retval, const uint8_t* client_data_buf, size_t client_data_buf_size, uint8_t* result, size_t client_data_size)
{
	sgx_status_t status;
	ms_upload_and_psi_t ms;
	ms.ms_client_data_buf = client_data_buf;
	ms.ms_client_data_buf_size = client_data_buf_size;
	ms.ms_result = result;
	ms.ms_client_data_size = client_data_size;
	status = sgx_ecall(eid, 1, &ocall_table_Enclave, &ms);
	if (status == SGX_SUCCESS && retval) *retval = ms.ms_retval;
	return status;
}

